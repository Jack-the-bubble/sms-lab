void mandelbrot (){	
	static uint8_t flag = 0;//flaga do zmiany trybu

	static int Px = 0;
	static int Py = 0;
	static float x = 0;
	static float xtemp = 0;
	static float y = 0;
	static float x0 = 0;
	static float y0 = 0;
	static uint8_t iteration = 0;
	// obszar rysowany zbioru Mandelbrota
	static float xmin = -0.1011 -0.01;
	static float xmax = -0.1011+0.01;
	static float ymin = 0.9563 -0.01;
	static float ymax = 0.9563+0.01;
	// for(Px=nr pierwszej kolumny pixeli ; Px <= nr ostatniej kolumny pixeli ; ++ Px)
	for (Px=22;Px<221;++ Px){ 
	// for(Py=nr pierwszego wiersza pixeli ; Px <= nr ostatniego wiersza pixeli ; ++ Py)
		for (Py = 33;Py<232;++ Py){ 
			
			x0 = (Px-22)*(xmax-xmin)/198+xmin;
			y0 = (Py-33)*(ymax-ymin)/198+ymin;	
			
		// algorytm rysujacy zbior Mandelbrota
		x = 0.0;
		y = 0.0;
		iteration = 0;
		while ((x*x + y*y < 2*2) && ( iteration < 0xFF)) {
			xtemp = x*x - y*y + x0;
			y = 2*x*y + y0;
			x = xtemp ;
			iteration = iteration + 1;
		}
		// rysowanie piksela w punkcie Px , Py ,
	  // o kolorze zaleznym od wartosci iteration (0 x00 -0 xFF)
		// TODO
			uint32_t color = 0x00000000;
			if(flag==0)//jesli od bialego do czerwonego
			{
				color |= 0xFF;//kanal alpha
				color <<= 8;
				color |= 0xFF;//kolor czerwony
				color <<= 8;
				color |= 0xFF-iteration;//kolor zielony
				color <<= 8;
				color |= 0xFF-iteration;//kolor niebieski
			}
			else//jesli od czarnego do zielonego
			{
				color |= 0xFF;//kanal alpha
				color <<= 8;
				color |= 0x00;//kolor czerwony
				color <<= 8;
				color |= iteration;//kolor zielony
				color <<= 8;
				color |= 0x00;//kolor niebieski
			}
			BSP_LCD_DrawPixel ( Px , Py , color );
		
		}
	}
	//Zmiana trybu do obslugi kolejnego wcisniecia przycisku
	flag=~flag;
	flag &= 0x01;
}

void voltageChart()
{
	static int Ry = 0; //piksel odpowiadajacy odczytanej wartosci napiecia
	static int it = 0; //iterator w petli przesuwania pikseli w lewo
	
	//Przesuwanie wszyskich ostatnich odczytow w lewo
	BSP_LCD_DrawPixel(262+197, buffer[it], LCD_COLOR_WHITE);
	for (it = 0; it < 197; ++it)
	{
		BSP_LCD_DrawPixel(it+262, buffer[it], LCD_COLOR_WHITE);
		buffer[it] = buffer[it+1];
		BSP_LCD_DrawPixel(it+262, buffer[it], LCD_COLOR_RED);
	}
	
	//Odczyt aktualnej wartosci i zapis do ostatniej kolumny wykresu
	Ry = 232 - adc_value/21;
	buffer[197] = Ry;
	//Ry = wymiar wyswietlacza w pionie - miejsce na przycisk i marginesy - adc_value znormalizowana do 198px
	
	BSP_LCD_DrawPixel(459, Ry, LCD_COLOR_RED);
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* AdcHandle)
{
	static int i=0; 
	static uint32_t tmpval= 0;
	for(i=0,tmpval=0;i<ADC_BUFFER_LENGTH; ++i){
		tmpval += uhADCxConvertedValue[i];
	}
	adc_value = tmpval/ADC_BUFFER_LENGTH;
}

